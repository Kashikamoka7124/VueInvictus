    {
          $data = DB::table('data')->first();
          DB::table('data')->where('id',$data->id)->update([
            'projects' => $request->projects,
            'cups' => $request->cups,
            'clients' => $request->clients,
            'affiliations' => $request->affiliations
          ]);
